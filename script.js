      function updateStory(key) {
        const currentStory = story[key];
        storyText.textContent = '';  // Clear previous text
        optionsContainer.innerHTML = '';
        
        let i = 0;
        function typeText() {
            if (i < currentStory.text.length) {
                storyText.textContent += currentStory.text.charAt(i);
                i++;
                setTimeout(typeText, 15);
            } else {
                // Add options once text typing is complete
                for (const [optionText, nextKey] of Object.entries(currentStory.options)) {
                    const button = document.createElement('button');
                    button.textContent = optionText;
                    button.onclick = () => updateStory(nextKey);
                    optionsContainer.appendChild(button);
                }
            }
        }
        typeText();
    }

    updateStory('start');
